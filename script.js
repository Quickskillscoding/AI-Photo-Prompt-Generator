/* ── GENERATE THE PROMPT ── */
function hpgGenerate() {
  const style = document.getElementById('hpg-style').value;
  const subject = document.getElementById('hpg-subject').value;
  const mood = document.getElementById('hpg-mood').value;
  const light = document.getElementById('hpg-light').value;
  const platform = document.getElementById('hpg-platform').value;

  // Get prompts array
  const prompts = bank[style][subject];
  if (!prompts) return;

  // No-repeat regenerate logic
  const key = `${style}_${subject}`;
  let index;
  if (prompts.length > 1) {
    const lastIndex = lastIndices[key];
    do {
      index = Math.floor(Math.random() * prompts.length);
    } while (index === lastIndex);
  } else {
    index = 0;
  }
  lastIndices[key] = index;
  const basePrompt = prompts[index];

  // Modifiers
  const activeMods = [];
  document.querySelectorAll('.hpg-tag.on').forEach(tag => {
    activeMods.push(tag.getAttribute('data-v'));
  });

  // Platform specific aspect ratio
  const platformSuffixes = {
    general: "",
    instagram_feed: "aspect ratio 4:5, Instagram feed layout",
    instagram_stories: "aspect ratio 9:16, mobile vertical view",
    facebook: "aspect ratio 4:5, Facebook post style",
    twitter: "aspect ratio 16:9, Twitter/X landscape style",
    pinterest: "aspect ratio 2:3, Pinterest vertical layout",
    youtube: "aspect ratio 16:9, cinematic widescreen",
    tiktok: "aspect ratio 9:16, TikTok full-screen layout"
  };

  // Build parts
  const promptParts = [basePrompt];
  if (mood) promptParts.push(`${mood} mood`);
  if (light) promptParts.push(light);
  if (activeMods.length) promptParts.push(...activeMods);
  if (platformSuffixes[platform]) promptParts.push(platformSuffixes[platform]);

  // Clean trailing commas and merge
  const finalPrompt = promptParts
    .map(p => p.trim())
    .filter(p => p.length > 0)
    .join(', ')
    .replace(/, ,+/g, ',')
    .trim();

  // Display Result Box
  const resultBox = document.getElementById('hpg-result');
  const outputText = document.getElementById('hpg-out');
  outputText.textContent = finalPrompt;
  resultBox.classList.add('show');

  // Render chips
  const styleOption = document.querySelector(`#hpg-style option[value="${style}"]`);
  const subjectOption = document.querySelector(`#hpg-subject option[value="${subject}"]`);
  const platformOption = document.querySelector(`#hpg-platform option[value="${platform}"]`);

  const styleText = styleOption ? styleOption.textContent : style;
  const subjectText = subjectOption ? subjectOption.textContent : subject;
  const platformText = platformOption ? platformOption.textContent.split(' — ')[0].split(' / ')[0] : platform;

  const chipsContainer = document.getElementById('hpg-chips');
  chipsContainer.innerHTML = `
    <span class="hpg-chip hpg-chip-a">${styleText}</span>
    <span class="hpg-chip hpg-chip-b">${subjectText}</span>
    <span class="hpg-chip hpg-chip-c">${platformText}</span>
  `;

  // Increment Statistics counter
  let count = parseInt(localStorage.getItem('hpg_count') || '0', 10);
  count++;
  localStorage.setItem('hpg_count', count);
  document.getElementById('hpg-count').textContent = count;

  // Add to History
  let history = JSON.parse(localStorage.getItem('hpg_history') || '[]');
  // Check if same prompt is already at index 0 to prevent exact duplicate history entries
  if (history[0] !== finalPrompt) {
    history.unshift(finalPrompt);
    if (history.length > 10) history = history.slice(0, 10);
    localStorage.setItem('hpg_history', JSON.stringify(history));
    hpgRenderHistory();
  }
}

/* ── COPY OUTPUT PROMPT ── */
function hpgCopy(btn) {
  const text = document.getElementById('hpg-out').textContent;
  if (!text) return;
  navigator.clipboard.writeText(text).then(() => {
    const originalText = btn.innerHTML;
    btn.textContent = "✓ Copied!";
    btn.style.backgroundColor = "#A5F302";
    btn.style.color = "#0f1400";
    setTimeout(() => {
      btn.innerHTML = originalText;
      btn.style.backgroundColor = "";
      btn.style.color = "";
    }, 2000);
  }).catch(err => {
    console.error('Could not copy text: ', err);
  });
}

/* ── COPY FROM HISTORY ── */
function hpgCopyText(event, text, btn) {
  event.stopPropagation(); // prevent loading item back
  navigator.clipboard.writeText(text).then(() => {
    const originalText = btn.textContent;
    btn.textContent = "✓ Copied";
    setTimeout(() => {
      btn.textContent = originalText;
    }, 2000);
  }).catch(err => {
    console.error('Could not copy text: ', err);
  });
}

/* ── RENDER RECENT HISTORY ── */
function hpgRenderHistory() {
  const history = JSON.parse(localStorage.getItem('hpg_history') || '[]');
  const container = document.getElementById('hpg-history');
  const listEl = document.getElementById('hpg-hist-list');

  if (history.length === 0) {
    container.classList.remove('show');
    return;
  }

  container.classList.add('show');
  listEl.innerHTML = '';

  history.forEach((promptText) => {
    const item = document.createElement('div');
    item.className = 'hpg-hist-item';
    
    // Clicking history item loads it back into output
    item.onclick = (e) => {
      if (e.target.classList.contains('hpg-hist-copy-btn')) return;
      
      document.getElementById('hpg-out').textContent = promptText;
      document.getElementById('hpg-result').classList.add('show');
      
      // Update Chips to indicate history loaded
      const chipsContainer = document.getElementById('hpg-chips');
      chipsContainer.innerHTML = `
        <span class="hpg-chip hpg-chip-c">Loaded from History</span>
      `;
      
      document.getElementById('hpg-result').scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    };

    const textEl = document.createElement('span');
    textEl.className = 'hpg-hist-text';
    textEl.textContent = promptText;

    const copyBtn = document.createElement('button');
    copyBtn.className = 'hpg-hist-copy-btn';
    copyBtn.textContent = 'Copy';
    copyBtn.onclick = (e) => hpgCopyText(e, promptText, copyBtn);

    item.appendChild(textEl);
    item.appendChild(copyBtn);
    listEl.appendChild(item);
  });
}
